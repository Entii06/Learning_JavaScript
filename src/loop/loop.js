//for
for (let i = 0; i <= 5; i++) {
  console.log("i = ", i);
}

//while
let score = 10;
while (score > 7) {
  console.log("score: ", score);
  score--;
}

// do-while
//chạy ít nhất 1 lần
let score_1 = 8;
do {
  console.log("score_1: ", score_1);
  score_1--;
} while (score_1 > 2);

